
public class SoundData {
 String id;
 double score;
 String label;
public SoundData(String id, double score, String label) {
	super();
	this.id = id;
	this.score = score;
	this.label = label;
}
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public double getScore() {
	return score;
}
public void setScore(double score) {
	this.score = score;
}
public String getLabel() {
	return label;
}
public void setLabel(String label) {
	this.label = label;
}
 
}
